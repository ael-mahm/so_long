#include "libft.h"

int	ft_lstsize(t_list	*lst)
{
	size_t	i;

	if (!lst)
		return (0);
	i = 1;
	while (lst->next != NULL)
	{
		lst = lst->next;
		i++;
	}
	return (i);
}
