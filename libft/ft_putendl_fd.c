#include "libft.h"

void	ft_putendl_fd(char *s, int fd)
{
	char	endl;

	if (!s || fd < 0)
		return ;
	endl = '\n';
	write (fd, s, ft_strlen(s));
	write (fd, &endl, 1);
}
